/*卡拉兹(Callatz)猜想：

对任何一个正整数 n，如果它是偶数，那么把它砍掉一半；如果它是奇数，那么把 (3n+1) 砍掉一半。
这样一直反复砍下去，最后一定在某一步得到 n=1。
卡拉兹在 1950 年的世界数学家大会上公布了这个猜想，
传说当时耶鲁大学师生齐动员，拼命想证明这个貌似很傻很天真的命题，结果闹得学生们无心学业，一心只证 (3n+1)，
以至于有人说这是一个阴谋，卡拉兹是在蓄意延缓美国数学界教学与科研的进展……

我们今天的题目不是证明卡拉兹猜想，而是对给定的任一不超过 1000 的正整数 n，
简单地数一下，需要多少步（砍几下）才能得到 n=1？
*/

#include<stdio.h>
int main()
{
	printf("对任何一个正整数 n，"
	"如果它是偶数，那么把它砍掉一半；\n"
	"如果它是奇数，那么把 (3n+1) 砍掉一半。\n" 
	"这样一直反复砍下去，最后一定在某一步得到 n=1。\n\n"
	"对给定的任一不超过 1000 的正整数 n，\n"
	"简单地数一下，需要多少步（砍几下）才能得到 n=1？\n\n"); 
	/*需要的变量*/
	int n;
	int cnt=0; 
	
	/*步骤*/
	
	/*输入数据*/ 
	//输入n < 1000 
	scanf("%d",&n);
	
	printf("\n");
	/*计算并输出*/ 
	while(n != 1)
	{
		//判断n是奇是偶
		if( n%2 )
			//  奇 (3n+1)/2 
			n = (3*n+1)/2; 
		else
		    //  偶      n/2
			n /= 2;
		
		//cnt ++
		cnt++;
	}
	
	printf("需要%d步",cnt);
}
